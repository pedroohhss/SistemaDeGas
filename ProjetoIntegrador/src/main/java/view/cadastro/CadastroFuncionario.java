    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:

        String textoValidadacao = "";
        if(jTextFieldNome.getText().trim().isEmpty()){
            textoValidadacao += "Informe o Nome. \n";
            jTextFieldNome.grabFocus();
        }
        if(jTextFieldCargo.getText().trim().isEmpty()){
            textoValidadacao += "Informe o Cargo. \n";
            jTextFieldNome.grabFocus();
        }
        if(jFormattedTextCPF.getText().trim().isEmpty()){
            textoValidadacao += "Informe o CPF. \n";
            jTextFieldNome.grabFocus();
        }
        if(jTextFieldEndereco.getText().trim().isEmpty()){
            textoValidadacao += "Informe o Endere√ßo do Funcionairio. \n";
            jTextFieldEndereco.grabFocus();
        }
        if(jFormattedTextTelefone.getText().trim().isEmpty()){
            textoValidadacao += "Informe o Telefone. \n";
            jFormattedTextTelefone.grabFocus();
        }
        if(textoValidadacao.isEmpty()) {
            Funcionario funcionario = new Funcionario(jTextFieldNome.getText().trim(),
                jTextFieldEndereco.getText().trim(),
                jTextFieldCargo.getText().trim(),
                jFormattedTextTelefone.getText().trim(),
                jFormattedTextCPF.getText().trim());
            FuncionarioDAO dao = new FuncionarioDAO();
            dao.inserirDados(funcionario);

            String textoSaida = "";
            textoSaida = "Nome: " + jTextFieldNome.getText() + " \n";
            textoSaida += "Endere√ßo: " + jTextFieldEndereco.getText() + " \n";
            textoSaida += "Cargo: " + jTextFieldCargo.getText() + " \n";
            textoSaida += "Telefone: " + jFormattedTextTelefone.getText() + " \n";
            textoSaida += "CPF: " + jFormattedTextCPF.getText() + " \n";
            JOptionPane.showMessageDialog(null, textoSaida,
                "Mensagem" , JOptionPane.INFORMATION_MESSAGE);
            jTextFieldNome.setText("");
            jTextFieldEndereco.setText("");
            jFormattedTextTelefone.setText("");
            jFormattedTextCPF.setText("");
            jTextFieldNome.grabFocus();
            this.jTable("SELECT * FROM funcionario ORDER BY id ASC");
        } else {
            JOptionPane.showMessageDialog(null, textoValidadacao,
                "Mensagem (Aviso)" , JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

