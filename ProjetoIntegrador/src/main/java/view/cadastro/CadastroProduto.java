    public void jTable(String sql) {
        try {
            String url = "jdbc:postgresql://localhost:5432/sistemaGas";
            String usuario = "postgres";
            String senha = "1234";
            conexao = DriverManager.getConnection(url, usuario, senha);
            PreparedStatement banco = conexao.prepareStatement(sql);
            banco.execute();
            ResultSet resultado = banco.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTableVisao.getModel();
            model.setNumRows(0);
            
            while(resultado.next()) {
                model.addRow(new Object[] {
                    resultado.getString("id"),
                    resultado.getString("nome"),
                    resultado.getString("preco"),
                    resultado.getString("valorcompra")
                });
            }
            banco.close();
            conexao.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

