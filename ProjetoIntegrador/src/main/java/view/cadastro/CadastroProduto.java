    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:

        String textoValidadacao = "";
        if(jTextFieldNome.getText().trim().isEmpty()){
            textoValidadacao += "Informe o Nome. \n";
            jTextFieldNome.grabFocus();
        }
        if(jTextFieldPreco.getText().trim().isEmpty()){
            textoValidadacao += "Informe o Preço de venda do produto. \n";
            jTextFieldPreco.grabFocus();
        }
        if(jTextFieldValorCompra.getText().trim().isEmpty()){
            textoValidadacao += "Informe o Valor de compra do produto. \n";
            jTextFieldValorCompra.grabFocus();
        }
        if(textoValidadacao.isEmpty()) {
            Produto cliente = new Produto(jTextFieldNome.getText().trim(),
                                            Double.parseDouble(jTextFieldPreco.getText().trim()),
                                            Double.parseDouble(jTextFieldValorCompra.getText().trim()));
            ProdutoDAO dao = new ProdutoDAO();
            dao.inserirDados(cliente);

            String textoSaida = "";
            textoSaida = "Nome: " + jTextFieldNome.getText() + " \n";
            textoSaida += "Preço de venda: " + jTextFieldPreco.getText() + " \n";
            textoSaida += "Valor de compra: " + jTextFieldValorCompra.getText() + " \n";
            JOptionPane.showMessageDialog(null, textoSaida,
                "Mensagem" , JOptionPane.INFORMATION_MESSAGE);
            jTextFieldNome.setText("");
            jTextFieldPreco.setText("");
            jTextFieldValorCompra.setText("");
            jTextFieldNome.grabFocus();
            this.jTable("SELECT * FROM produto ORDER BY id ASC");
        } else {
            JOptionPane.showMessageDialog(null, textoValidadacao,
                "Mensagem (Aviso)" , JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

